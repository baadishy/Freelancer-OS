/**
 * Polling controller declarations.
 */
let pollerActive = false;
let runningBotToken = '';

export function stopTelegramPolling() {
  pollerActive = false;
  runningBotToken = '';
}

export function startTelegramPolling() {
  const settings = db.getTelegramSettings();
  if (!settings.enabled || !settings.botToken) {
    db.addLog('info', 'telegram', 'Telegram bot polling skipped: Settings incomplete or disabled.');
    stopTelegramPolling();
    return;
  }

  // If the poller is already running and the token hasn't changed, reuse thread
  if (pollerActive && runningBotToken === settings.botToken) {
    return;
  }

  // If it's running with a DIFFERENT token, shut it down first so the old fetch completes
  if (pollerActive && runningBotToken !== settings.botToken) {
    db.addLog('info', 'telegram', 'Bot Token change detected. Terminating running loop and starting fresh...');
    pollerActive = false;
  }

  pollerActive = true;
  runningBotToken = settings.botToken;
  db.addLog('success', 'telegram', 'Telegram interactive chatbot loop initiated.');

  (async () => {
    let offset = 0;
    const initialToken = settings.botToken;

    while (pollerActive && runningBotToken === initialToken) {
      const currentSettings = db.getTelegramSettings();
      if (!currentSettings.enabled || !currentSettings.botToken || currentSettings.botToken !== initialToken) {
        db.addLog('info', 'telegram', 'Telegram bot thread exiting naturally due to configuration update.');
        break;
      }

      try {
        const response = await fetch(
          `https://api.telegram.org/bot${initialToken}/getUpdates?offset=${offset}&timeout=20`,
          { method: 'GET', signal: AbortSignal.timeout(30000) }
        ).catch(() => null);

        if (!response) {
          await new Promise(resolve => setTimeout(resolve, 5000));
          continue;
        }

        const data = await response.json() as any;
        if (!data.ok) {
          db.addLog('warning', 'telegram', `Telegram server update check blocked: ${data.description || 'check credentials'}`);
          await new Promise(resolve => setTimeout(resolve, 10000));
          continue;
        }

        const updates = data.result || [];
        for (const update of updates) {
          offset = update.update_id + 1;
          if (update.message) {
            await handleIncomingTelegramMessage(update.message);
          }
        }
      } catch (err: any) {
        await new Promise(resolve => setTimeout(resolve, 5000));
      }

      await new Promise(resolve => setTimeout(resolve, 500));
    }

    if (runningBotToken === initialToken) {
      pollerActive = false;
      runningBotToken = '';
    }
  })();
}

/**
 * Active Scheduler check loop to call daily briefing
 */
let schedulerIntervalId: NodeJS.Timeout | null = null;
let lastBriefingDateStr = '';

export function startScheduler() {
  if (schedulerIntervalId) {
    clearInterval(schedulerIntervalId);
  }

  // Automatically start/refresh Telegram Bot polling loop
  startTelegramPolling();

  // Check every 60s
  schedulerIntervalId = setInterval(async () => {
    const settings = db.getTelegramSettings();
    if (!settings.enabled || !settings.botToken || !settings.chatId) return;

    const now = new Date();
    const currentHourMin = `${String(now.getHours()).padStart(2, '0')}:${String(now.getMinutes()).padStart(2, '0')}`;
    const todayDateStr = now.toDateString();

    if (currentHourMin === settings.reportTime && lastBriefingDateStr !== todayDateStr) {
      lastBriefingDateStr = todayDateStr;
      db.addLog('info', 'telegram', `Triggering scheduled Daily Briefing Report at ${settings.reportTime}`);
      await sendDailyBriefingReport();
    }
  }, 60000);
}
