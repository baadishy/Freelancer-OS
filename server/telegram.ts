export function escapeMarkdown(str: string): string {
  if (!str) return '';
  return str
    .replace(/\\/g, '\\\\')
    .replace(/\*/g, '\\*')
    .replace(/_/g, '\\_')
    .replace(/\[/g, '\\[')
    .replace(/\]/g, '\\]')
    .replace(/\(/g, '\\(')
    .replace(/\)/g, '\\)')
    .replace(/`/g, '\\`');
}

export function escapeHtml(str: string): string {
  if (!str) return '';
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

/**
 * Send a message via the Telegram Bot API safely.
 */
export async function sendTelegramMessage(message: string, parseMode: 'HTML' | 'Markdown' = 'HTML'): Promise<boolean> {
  const settings = db.getTelegramSettings();
  if (!settings.enabled || !settings.botToken || !settings.chatId) {
    // Gracefully skip if Telegram is offline/incomplete
    return false;
  }

  try {
    const response = await fetch(
      `https://api.telegram.org/bot${settings.botToken}/sendMessage`,
      {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          chat_id: settings.chatId,
          text: message,
          parse_mode: parseMode,
        }),
      }
    );

    const result = await response.json() as any;
    if (result.ok) {
      db.addLog('success', 'telegram', 'Notification successfully dispatched via Telegram Bot.');
      return true;
    } else {
      db.addLog(
        'warning',
        'telegram',
        `Telegram endpoint returned rejection: ${result.description || 'Unknown error'}`
      );
      return false;
    }
  } catch (err: any) {
    db.addLog(
      'error',
      'telegram',
      `Failed to connect to Telegram server: ${err?.message || 'Connection refused'}`
    );
    return false;
  }
}

/**
 * Send custom reply direct in chatbot thread.
 */
async function sendReply(chatId: string, replyText: string, parseMode: 'HTML' | 'Markdown' = 'HTML'): Promise<boolean> {
  const settings = db.getTelegramSettings();
  if (!settings.botToken) return false;

  try {
    const response = await fetch(
      `https://api.telegram.org/bot${settings.botToken}/sendMessage`,
      {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          chat_id: chatId,
          text: replyText,
          parse_mode: parseMode,
          disable_web_page_preview: true
        }),
      }
    );
    const result = await response.json() as any;
    return !!result.ok;
  } catch (err: any) {
    console.error('Failed sending chatbot response:', err);
    return false;
  }
}

